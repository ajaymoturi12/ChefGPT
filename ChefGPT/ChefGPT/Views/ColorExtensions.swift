import Foundation
import SwiftUI


extension Color {
    
    public static func GPTlightGrey() -> Color {
        Color.init(red: 214/255, green: 214/255, blue: 214/255)
    }
    
    public static func GPTorange() -> Color {
        Color.init(red: 237/255, green: 104/255, blue: 46/255)
    }
    
    
}
