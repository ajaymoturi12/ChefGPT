import Foundation
import SwiftUI


extension Color {
    
    public static func GPTlightGrey() -> Color {
        Color.init(red: 217/255, green: 217/255, blue: 217/255)
    }
    
    public static func GPTorange() -> Color {
        Color.init(red: 237/255, green: 104/255, blue: 46/255)
    }
    
    public static func GPTdarkGrey() -> Color {
        Color.init(red: 142/255, green: 142/255, blue: 142/255)
    }
    
    public static func GPTlightGreen() -> Color { // #BDE6AD
        Color.init(red: 189/255, green: 230/255, blue: 173/255)
    }
}
